package Model;

public class Door {

    private String name;
    private String dest;

    public void setDest(String dest) {
        this.dest = dest;
    }

    public void setName(String location) {
        this.name = location;
    }

    public String getName() {
        return name;
    }

    public String getDest() {
        return dest;
    }
}
