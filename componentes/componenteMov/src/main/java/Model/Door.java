package Model;

public class Door {

    private String location;
    private String dest;

    public String getName() {
        return location;
    }

    public void setName(String location) {
        this.location = location;
    }

    public String getDest() {
        return dest;
    }

    public void setDest(String dest) {
        this.dest = dest;
    }
}
